package beans;

/**
 *
 * @author Brana
 */
public class Rent {
    private String n, o;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getO() {
        return o;
    }

    public void setO(String o) {
        this.o = o;
    }

    public Rent() {
    }

    public Rent(String n, String o) {
        this.n = n;
        this.o = o;
    }
    
}
